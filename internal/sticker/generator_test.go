func TestExtractDominantColor(t *testing.T) {
	// Create a simple test image with red edges
	img := image.NewRGBA(image.Rect(0, 0, 100, 100))
	red := color.RGBA{255, 0, 0, 255}

	// Fill edges with red
	for x := 0; x < 100; x++ {
		img.Set(x, 0, red)  // Top edge
		img.Set(x, 99, red) // Bottom edge
	}
	for y := 0; y < 100; y++ {
		img.Set(0, y, red)  // Left edge
		img.Set(99, y, red) // Right edge
	}

	gen := NewGenerator(false)
	dominantColor := gen.extractDominantColor(img)

	r, g, b, _ := dominantColor.RGBA()
	// Convert from 16-bit to 8-bit
	r8, g8, b8 := uint8(r>>8), uint8(g>>8), uint8(b>>8) //nolint:gosec // Shifting by 8 ensures values fit in uint8

	// Should be reddish (darkened by 20%)
	if r8 < 150 || g8 > 50 || b8 > 50 {
		t.Errorf("Expected reddish color, got R=%d G=%d B=%d", r8, g8, b8)
	}
}

func TestGeneratePDF(t *testing.T) {
	gen := NewGenerator(false)

	// Create test mappings
	mappings := []*models.CardMapping{
		{
			ID:           1,
			MediaTitle:   "Test Movie",
			MediaType:    "movie",
			ThumbnailURL: "", // Empty URL for placeholder test
		},
	}

	pdfBytes, err := gen.GeneratePDF(mappings, "test-token")
	if err != nil {
		t.Fatalf("GeneratePDF failed: %v", err)
	}

	if len(pdfBytes) == 0 {
		t.Fatal("GeneratePDF returned empty bytes")
	}

	// Verify PDF signature
	if !bytes.HasPrefix(pdfBytes, []byte("%PDF")) {
		t.Fatal("Generated bytes are not a valid PDF")
	}
}
