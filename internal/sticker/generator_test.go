func TestFetchPosterImage(t *testing.T) {
	// Create a simple 1x1 test image programmatically
	testImg := image.NewRGBA(image.Rect(0, 0, 1, 1))
	testImg.Set(0, 0, color.RGBA{255, 0, 0, 255}) // Red pixel

	// Encode to PNG
	var buf bytes.Buffer
	if err := png.Encode(&buf, testImg); err != nil {
		t.Fatalf("Failed to encode test image: %v", err)
	}
	pngData := buf.Bytes()

	server := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, _ *http.Request) {
		w.Header().Set("Content-Type", "image/png")
		_, _ = w.Write(pngData)
	}))
	defer server.Close()

	gen := NewGenerator(true)
	img, err := gen.fetchPosterImage(server.URL)
	if err != nil {
		t.Fatalf("fetchPosterImage failed: %v", err)
	}
	if img == nil {
		t.Fatal("fetchPosterImage returned nil image")
	}
}

